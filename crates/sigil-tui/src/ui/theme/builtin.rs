use super::palette::ThemePalette;

pub(crate) fn palette_for(id: ThemeId) -> ThemePalette {
    match id {
        ThemeId::SigilDark => sigil_dark(),
        ThemeId::SolarizedDark => solarized_dark(),
        ThemeId::SolarizedLight => solarized_light(),
        ThemeId::GruvboxDark => gruvbox_dark(),
        ThemeId::Nord => nord(),
        ThemeId::HighContrastDark => high_contrast_dark(),
    }
}

fn c(red: u8, green: u8, blue: u8) -> Color {
    Color::Rgb(red, green, blue)
}

