export interface LocaleContextValue {
  locale: Locale;
  t: (key: MessageKey, values?: Record<string, string | number>) => string;
  toggleLocale: () => void;
}

export type Translate = LocaleContextValue["t"];

export const translateEnglish: Translate = (key, values) => interpolate(en[key], values);

const LocaleContext = createContext<LocaleContextValue>({
  locale: "en",
  t: translateEnglish,
  toggleLocale: () => undefined,
});

export function LocaleProvider({ children }: { readonly children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(readInitialLocale);
  useEffect(() => {
    document.documentElement.lang = locale;
    try {
      window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    } catch {
      // Locale persistence is best effort; the selected language stays active for this window.
    }
  }, [locale]);
  const value = useMemo<LocaleContextValue>(() => ({
    locale,
    t: (key, values) => interpolate((locale === "zh-CN" ? zh : en)[key], values),
    toggleLocale: () => setLocale((current) => current === "en" ? "zh-CN" : "en"),
  }), [locale]);
  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale(): LocaleContextValue {
  return useContext(LocaleContext);
}

function readInitialLocale(): Locale {
  try {
    const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    if (stored === "en" || stored === "zh-CN") return stored;
  } catch {
    // Fall through to the browser preference.
  }
  return window.navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

function interpolate(template: string, values?: Record<string, string | number>): string {
  if (values === undefined) return template;
  return template.replace(/\{([^}]+)\}/g, (_match, key: string) => String(values[key] ?? ""));
}
